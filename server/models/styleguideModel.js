import mongoose from "mongoose";

const styleGuideSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const StyleGuide = mongoose.model("StyleGuide", styleGuideSchema);

export default StyleGuide;
